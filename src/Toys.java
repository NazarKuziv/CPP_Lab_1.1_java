public class Toys {
    public String Name;
    public String Price;
    public String Age_From;
    public String Age_To;
    public String Characteristic;


    public String getName() {
        return Name;
    }

    public void setName(String newName) {
        this.Name = newName;
    }

    public String getPrice() {
        return Price;
    }

    public void setPrice(String newPrice) {
        this.Price = newPrice;
    }

    public String getAge_From() {
        return Age_From;
    }

    public void setAge_From(String newAge_From) {
        this.Age_From = newAge_From;
    }

    public String getAge_To() {
        return Age_To;
    }

    public void setAge_To(String newAge_To) {
        this.Age_To = newAge_To;
    }

    public String getCharacteristic() {
        return Characteristic;
    }

    public void setCharacteristic(String newCharacteristic) {
        this.Characteristic = newCharacteristic;
    }

}
